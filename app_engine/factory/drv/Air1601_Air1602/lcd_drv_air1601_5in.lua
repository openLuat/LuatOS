		port      = port,
		pin_clk   = 23, -- SPI 时钟引脚 (用于初始化)
		pin_sda   = 22, -- SPI 数据引脚
		pin_cs    = 2, -- SPI 片选引脚
		pin_rst   = pr,
		direction = 0,
		w         = 720,
		h         = 1280,
		xoffset   = 0,
		yoffset   = 0,
		hbp       = 46,
		hspw      = 2,
		hfp       = 48,
		vbp       = 24,
		vspw      = 2,
		vfp       = 24,
		bus_speed = 62 * 1000 * 1000,
	})

